fn report_fixpoint_errors(
    genv: GlobalEnv,
    local_id: LocalDefId,
    errors: Vec<(Tag, TagIdx)>,
) -> Result<(), ErrorGuaranteed> {
    #[expect(clippy::collapsible_else_if, reason = "it looks better")]
    if genv.should_fail(local_id) {
        if errors.is_empty() { report_expected_neg(genv, local_id) } else { Ok(()) }
    } else {
        if errors.is_empty() { Ok(()) } else { report_errors(genv, local_id, errors) }
    }
}

fn check_body(
    genv: GlobalEnv,
    cache: &mut FixQueryCache,
    def_id: LocalDefId,
    poly_sig: &rty::PolyFnSig,
) -> Result<(), ErrorGuaranteed> {
    let span = genv.tcx().def_span(def_id);
    let opts = genv.infer_opts(def_id);

    dbg::log_verbose!("FLUX checking: {def_id:?} {span:?}");

    let ghost_stmts = compute_ghost_statements(genv, def_id)
        .with_span(span)
        .map_err(|err| err.emit(genv, def_id))?;
    let mut closures = UnordMap::default();

    // PHASE 1: infer shape of `TypeEnv` at the entry of join points
    let shape_result =
        Checker::run_in_shape_mode(genv, def_id, &ghost_stmts, &mut closures, opts, poly_sig)
            .map_err(|err| err.emit(genv, def_id))?;

    // PHASE 2: generate refinement tree constraint
    let infcx_root = Checker::run_in_refine_mode(
        genv,
        def_id,
        &ghost_stmts,
        &mut closures,
        shape_result,
        opts,
        poly_sig,
    )
    .map_err(|err| err.emit(genv, def_id))?;

    // PHASE 3: invoke fixpoint on the constraint
    if (genv.proven_externally(def_id).is_some() && flux_config::lean().is_check())
        || flux_config::lean().is_emit()
    {
        infcx_root
            .execute_lean_query(cache, MaybeExternId::Local(def_id))
            .emit(&genv)
    } else {
        let answer = infcx_root
            .execute_fixpoint_query(cache, MaybeExternId::Local(def_id), FixpointQueryKind::Body)
            .emit(&genv)?;

        let tcx = genv.tcx();
        let hir_id = tcx.local_def_id_to_hir_id(def_id);
        let body_span = tcx.hir_span_with_body(hir_id);
        dbg::solution!(genv, &answer, body_span);

        let errors = answer.errors;
        report_fixpoint_errors(genv, def_id, errors)
    }
}

pub fn check_static(
    genv: GlobalEnv,
    cache: &mut FixQueryCache,
    def_id: LocalDefId,
    ty: rty::Ty,
) -> Result<(), ErrorGuaranteed> {
    // Build a PolyFnSig with no inputs and `ty` as the output
    let output = rty::Binder::dummy(rty::FnOutput::new(ty, vec![]));
    let fn_sig = rty::FnSig::new(
        rustc_hir::Safety::Safe,
        rustc_abi::ExternAbi::Rust,
        rty::List::empty(),
        rty::List::empty(),
        output,
        rty::Expr::ff(),
        false,
    );
    let poly_sig = rty::PolyFnSig::dummy(fn_sig);

    metrics::incr_metric(Metric::FnChecked, 1);
    metrics::time_it(TimingKind::CheckBody(def_id), || check_body(genv, cache, def_id, &poly_sig))
}

pub fn check_fn(
    genv: GlobalEnv,
    cache: &mut FixQueryCache,
    def_id: LocalDefId,
) -> Result<(), ErrorGuaranteed> {
    let span = genv.tcx().def_span(def_id);

    // HACK(nilehmann) this will ignore any code generated by a macro. This is a temporary
    // workaround to deal with a `#[derive(..)]` that generates code that flux cannot handle.
    // Note that this is required because code generated by a `#[derive(..)]` cannot be
    // manually trusted or ignored.
    if !genv.tcx().def_span(def_id).ctxt().is_root() {
        metrics::incr_metric(Metric::FnTrusted, 1);
        return Ok(());
    }

    let opts = genv.infer_opts(def_id);

    // FIXME(nilehmann) we should move this check to `compare_impl_item`
    if let Some(infcx_root) = trait_impl_subtyping(genv, def_id, opts, span)
        .with_span(span)
        .map_err(|err| err.emit(genv, def_id))?
    {
        tracing::info!("check_fn::refine-subtyping");
        let answer = infcx_root
            .execute_fixpoint_query(cache, MaybeExternId::Local(def_id), FixpointQueryKind::Impl)
            .emit(&genv)?;
        tracing::info!("check_fn::fixpoint-subtyping");
        let errors = answer.errors;
        report_fixpoint_errors(genv, def_id, errors)?;
    }

    // Skip trusted functions
    if genv.trusted(def_id) {
        metrics::incr_metric(Metric::FnTrusted, 1);
        return Ok(());
    }

    metrics::incr_metric(Metric::FnChecked, 1);
    metrics::time_it(TimingKind::CheckBody(def_id), || -> Result<(), ErrorGuaranteed> {
        let poly_sig = genv
            .fn_sig(def_id)
            .with_span(span)
            .map_err(|err| err.emit(genv, def_id))?
            .instantiate_identity();
        let poly_sig = rty::auto_strong(genv, def_id, poly_sig);

        check_body(genv, cache, def_id, &poly_sig)
    })?;

    dbg::check_fn_span!(genv.tcx(), def_id).in_scope(|| Ok(()))
}

fn report_errors(
    genv: GlobalEnv,
    local_id: LocalDefId,
    errors: Vec<(Tag, TagIdx)>,
) -> Result<(), ErrorGuaranteed> {
    let log_path = if config::dump_constraint() {
        let path = dbg::item_dump_path(genv.tcx(), local_id.to_def_id(), "smt2");
        if path.exists() { Some(path) } else { None }
    } else {
        None
    };

    let mut e = None;
    for (err, tag_idx) in errors {
        let span = err.src_span;
        let emit = |mut diag: rustc_errors::Diag<'_, ErrorGuaranteed>| -> ErrorGuaranteed {
            if let Some(path) = &log_path {
                diag.arg("path", path.display().to_string());
                diag.arg("tag", tag_idx.to_string());
                diag.note(crate::fluent_generated::refineck_constraint_log_note);
            }
            diag.emit()
        };
        let dcx = genv.sess().dcx().handle();
        e = Some(match err.reason {
            ConstrReason::Call
            | ConstrReason::Subtype(SubtypeReason::Input)
            | ConstrReason::Subtype(SubtypeReason::Requires)
            | ConstrReason::Predicate => {
                emit(
                    errors::RefineError::call(span, err.dst_span)
                        .into_diag(dcx, rustc_errors::Level::Error),
                )
            }
            ConstrReason::Assign => {
                emit(errors::AssignError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Ret
            | ConstrReason::Subtype(SubtypeReason::Output)
            | ConstrReason::Subtype(SubtypeReason::Ensures) => {
                emit(
                    errors::RefineError::ret(span, err.dst_span)
                        .into_diag(dcx, rustc_errors::Level::Error),
                )
            }
            ConstrReason::Div => {
                emit(errors::DivError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Rem => {
                emit(errors::RemError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Goto(_) => {
                emit(errors::GotoError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Assert(msg) => {
                emit(errors::AssertError { span, msg }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Fold | ConstrReason::FoldLocal => {
                emit(
                    errors::FoldError::new(span, err.dst_span)
                        .into_diag(dcx, rustc_errors::Level::Error),
                )
            }
            ConstrReason::Overflow => {
                emit(errors::OverflowError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Underflow => {
                emit(errors::UnderflowError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::Other => {
                emit(errors::UnknownError { span }.into_diag(dcx, rustc_errors::Level::Error))
            }
            ConstrReason::NoPanic { callee, caller, location } => {
                emit_panic_error(genv, dcx, span, callee, caller, location, emit)
            }
        });
    }

    if let Some(e) = e { Err(e) } else { Ok(()) }
}

fn report_expected_neg(genv: GlobalEnv, def_id: LocalDefId) -> Result<(), ErrorGuaranteed> {
    Err(genv.sess().emit_err(errors::ExpectedNeg {
        span: genv.tcx().def_span(def_id),
        def_descr: genv.tcx().def_descr(def_id.to_def_id()),
    }))
}

fn emit_panic_error(
    genv: GlobalEnv,
    dcx: rustc_errors::DiagCtxtHandle<'_>,
    span: rustc_span::Span,
    callee: rustc_span::def_id::DefId,
    caller: Option<rustc_span::def_id::DefId>,
    location: Option<rustc_middle::mir::Location>,
    emit: impl FnOnce(rustc_errors::Diag<'_, ErrorGuaranteed>) -> ErrorGuaranteed,
) -> ErrorGuaranteed {
    let spec = genv.inferred_no_panic_at(caller, location);
    let (hops, reason) = collect_panic_chain(genv, spec);
    let mut diag =
        errors::PanicError { span, callee: genv.tcx().def_path_debug_str(callee), reason }
            .into_diag(dcx, rustc_errors::Level::Error);
    for (hop_span, msg) in hops {
        diag.span_note(hop_span, msg);
    }
    emit(diag)
}

/// Walks the `Transitive` blame chain in `spec`, collecting a `span_note` per hop and
/// producing a short root-cause label for the primary error message.
///
/// Returns `(hops, reason)` where each hop is `(call_site_span, note_text)` and `reason`
/// names the terminal variant, prefixed with `"Transitive -> "` if any hops were traversed
/// (e.g. `"Transitive -> SynthesizedPanic"`, `"UnresolvedCall(\`foo\`)"`)
fn collect_panic_chain<'tcx>(
    genv: GlobalEnv<'_, 'tcx>,
    mut spec: PanicSpec<'tcx>,
) -> (Vec<(rustc_span::Span, String)>, String) {
    let mut hops = Vec::new();
    let mut transitive = false;
    const MAX_DEPTH: usize = 20;
    for _ in 0..MAX_DEPTH {
        match spec {
            PanicSpec::WillNotPanic => break,
            PanicSpec::MightPanic(reason) => {
                match reason {
                    PanicReason::Transitive { callee, call_site } => {
                        let name = genv.tcx().def_path_debug_str(callee.def_id());
                        hops.push((call_site, format!("which calls `{name}` here")));
                        spec = genv.inferred_no_panic_key(callee);
                        transitive = true;
                    }
                    PanicReason::UnresolvedCall { def_id, call_site } => {
                        let name = genv.tcx().def_path_debug_str(def_id);
                        hops.push((
                            call_site,
                            format!("cannot resolve `{name}` to a concrete implementation"),
                        ));
                        return (hops, root_cause(transitive, format!("UnresolvedCall(`{name}`)")));
                    }
                    PanicReason::DynamicDispatch { call_site } => {
                        hops.push((call_site, "calls a function pointer or closure".into()));
                        return (hops, root_cause(transitive, "DynamicDispatch".into()));
                    }
                    PanicReason::SynthesizedPanic { call_site } => {
                        hops.push((call_site, "Rustc-synthesized panic here (likely arithmetic overflow, bounds check, div-by-zero, or assert! macro)".into()));
                        return (hops, root_cause(transitive, "SynthesizedPanic".into()));
                    }
                    PanicReason::NotInCallGraph => {
                        return (hops, root_cause(transitive, "NotInCallGraph".into()));
                    }
                    PanicReason::NoMIRAvailable => {
                        return (hops, root_cause(transitive, "NoMIRAvailable".into()));
                    }
                }
            }
        }
    }
    (hops, "unknown".into())
}

fn root_cause(transitive: bool, cause: String) -> String {
    if transitive { format!("Transitive -> {cause}") } else { cause }
}

mod errors {
    use flux_errors::E0999;
    use flux_macros::{Diagnostic, Subdiagnostic};
    use flux_middle::rty::ESpan;
    use rustc_span::Span;

    #[derive(Diagnostic)]
    #[diag(refineck_goto_error, code = E0999)]
    pub struct GotoError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_assign_error, code = E0999)]
    pub struct AssignError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Subdiagnostic)]
    #[note(refineck_condition_span_note)]
    pub(crate) struct ConditionSpanNote {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Subdiagnostic)]
    #[note(refineck_call_span_note)]
    pub(crate) struct CallSpanNote {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_refine_error, code = E0999)]
    pub struct RefineError {
        #[primary_span]
        #[label]
        pub span: Span,
        cond: &'static str,
        #[subdiagnostic]
        span_note: Option<ConditionSpanNote>,
        #[subdiagnostic]
        call_span_note: Option<CallSpanNote>,
    }

    impl RefineError {
        pub fn call(span: Span, espan: Option<ESpan>) -> Self {
            RefineError::new("precondition", span, espan)
        }

        pub fn ret(span: Span, espan: Option<ESpan>) -> Self {
            RefineError::new("postcondition", span, espan)
        }

        fn new(cond: &'static str, span: Span, espan: Option<ESpan>) -> RefineError {
            match espan {
                Some(dst_span) => {
                    let span_note = Some(ConditionSpanNote { span: dst_span.span });
                    let call_span_note = dst_span.base.map(|span| CallSpanNote { span });
                    RefineError { span, cond, span_note, call_span_note }
                }
                None => RefineError { span, cond, span_note: None, call_span_note: None },
            }
        }
    }

    #[derive(Diagnostic)]
    #[diag(refineck_div_error, code = E0999)]
    pub struct DivError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_rem_error, code = E0999)]
    pub struct RemError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_assert_error, code = E0999)]
    pub struct AssertError {
        #[primary_span]
        pub span: Span,
        pub msg: &'static str,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_fold_error, code = E0999)]
    pub struct FoldError {
        #[primary_span]
        pub span: Span,
        #[subdiagnostic]
        span_note: Option<ConditionSpanNote>,
    }

    impl FoldError {
        pub fn new(span: Span, espan: Option<ESpan>) -> Self {
            let span_note = espan.map(|espan| ConditionSpanNote { span: espan.span });
            FoldError { span, span_note }
        }
    }

    #[derive(Diagnostic)]
    #[diag(refineck_overflow_error, code = E0999)]
    pub struct OverflowError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_underflow_error, code = E0999)]
    pub struct UnderflowError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_unknown_error, code = E0999)]
    pub struct UnknownError {
        #[primary_span]
        pub span: Span,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_expected_neg, code = E0999)]
    pub struct ExpectedNeg {
        #[primary_span]
        pub span: Span,
        pub def_descr: &'static str,
    }

    #[derive(Diagnostic)]
    #[diag(refineck_panic_error, code = E0999)]
    pub(super) struct PanicError {
        #[primary_span]
        pub(super) span: Span,
        pub(super) callee: String,
        pub(super) reason: String,
    }
}
